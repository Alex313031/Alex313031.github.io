double Color::blackbodyWavelength(double temperature, double uniform)
{
    // Find the first element that is >= 'uniform'.
    const float *f = std::lower_bound( &blackbodyCDF[0],
        &blackbodyCDF[ sizeof blackbodyCDF / sizeof blackbodyCDF[0] ], uniform);

    // Adjust for underflow
    if (f == &blackbodyCDF[0])
        f++;

    // Values >= and <= the requested uniform
    double upper = f[0];
    double lower = f[-1];

    // Linear interpolation
    double nm = int(f - &blackbodyCDF[0]) + (uniform - lower) / (upper - lower);

    // Scale to 'temperature' using Wein's displacement law
    nm *= kBlackbodyCDFTemperature / temperature;

    return nm;
}

void Color::testSpectrum(double temperature)
{
    // Test code for our blackbodyWavelength() transform.

    // Wavelength buckets, 1/10 nanometer wide.
    const unsigned numBuckets = 10000;
    unsigned buckets[numBuckets];
    memset(buckets, 0, sizeof buckets);

    printf("Stochastic spectrum test for %fK:\n", temperature);

    for (double u = 0; u <= 1.0; u += 0.000001) {
        double nm = blackbodyWavelength(temperature, u);
        unsigned i = nm * 10;
        if (i < numBuckets)
            buckets[i]++;
    }

    for (unsigned i = 0; i < numBuckets; ++i) {
        unsigned count = buckets[i];
        if (count)
            printf("%5.1f\t%d\n", i * 0.1, count);
    }
}

void Color::setWavelength(double nm)
{
    if (nm == 0) {
        // Monochromatic white
        r = g = b = 8192;
        return;
    }

    if (nm < kFirstWavelength || nm > kLastWavelength) {
        // Invisible
        r = g = b = 0;
        return;
    }

    double fpIndex = nm - kFirstWavelength;
    int index = fpIndex;
    double frac = fpIndex - index;
    double inv = 1.0 - frac;
    index *= 3;

    int r1 = wavelengthToRGB[index++];
    int g1 = wavelengthToRGB[index++];
    int b1 = wavelengthToRGB[index++];
    int r2 = wavelengthToRGB[index++];
    int g2 = wavelengthToRGB[index++];
    int b2 = wavelengthToRGB[index  ];

    // Linear interpolation
    r = inv * r1 + frac * r2;
    g = inv * g1 + frac * g2;
    b = inv * b1 + frac * b2;
}
